day=int(input("Enter number in between 1 to 7 : "))
if(day==1):
    print("SUNDAY")
elif(day==2):
    print("MONDAY")
elif(day==3):
    print("TUESDAY")
elif(day==4):
    print("WEDNESDAY")
elif(day==5):
    print("THURSEDAY")
elif(day==6):
    print("FRIDAY")
elif(day==7):
    print("SATURDAY")
else:
    print("Wrong input")
